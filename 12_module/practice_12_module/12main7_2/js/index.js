// Создайте еще один массив с числами от 10 до 15.
// Используя цикл while, сложите соответствующие элементы двух массивов и выведите результат в консоль.
// Используя цикл for, пропустите вывод числа 13 из второго массива.

// Создание массива чисел от 1 до 5
var array1 = [1, 2, 3, 4, 5];

// Цикл for для вывода элементов массива
console.log("Элементы массива array1:");
for (var i = 0; i < array1.length; i++) {
    console.log(array1[i]);
}

// Создание второго массива чисел от 10 до 15
var array2 = [10, 11, 12, 13, 14, 15];

// Цикл while для сложения соответствующих элементов двух массивов
var sum = 0;
var j = 0;
while (j < array1.length && j < array2.length) {
    sum += array1[j] + array2[j];
    j++;
}
console.log("\nСумма соответствующих элементов массивов:", sum);

// Цикл for с continue для пропуска числа 13
console.log("\nЭлементы массива array2 с пропуском числа 13:");
for (var k = 0; k < array2.length; k++) {
    if (array2[k] === 13) {
        continue;
    }
    console.log(array2[k]);
}